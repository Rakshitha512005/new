public class loops {
    public static void main(String[] args)
    //while loop example
    {
        int i = 0;
        while (i < 5) {
            System.out.println("its a while loop" + " " + i);
            i++;
        }
        //do while loop example
        int b = 0;
        do {
            System.out.println("its a do...while loop" + " " + b);
            b++;
        }
        while (b < 5);

        //for loop example
        for (int a = 0; a < 5; a++) {
            System.out.println("its a for loop" + " " + a);

        }
    }
}
